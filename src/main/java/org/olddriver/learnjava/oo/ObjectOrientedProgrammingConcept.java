public class ObjectOrientedProgrammingConcept{
	/*
	 * 对象(Object)
	 * 软件对象和现实中对象类似，对象可理解为是一种奇特变量，由属性和行为构成
	 * 属性存储到字段中，行为定义到方法中
	 * 
	 * 类(Class)
	 * 类是对具有相同属性和行为的对象的抽象，类是创建对象的蓝图；
	 * 类成员包含字段(Field or Member variable)，方法(Method)，类(Class)，接口(Interface)。
	 * 类中还可以定义构造器(Constructor)，实例初始化器(InstanceInitializer)，静态初始化器(StaticInitializer)
	 * 类中static字段称为类变量，非static字段称为实例变量
	 * 类中static方法称为类方法，非static方法称为实例方法
	 * 
	 * 接口(Interface)
	 * 接口定义类与外界交互的规则。
	 * 接口成员包含字段(public static final修饰)，方法，类，接口
	 *
	 * 面向对象思想
	 * 将现实中对象抽象为类，在java程序中使用类的实例解决问题
	 *
	 * 面向对象程序特点
	 * java程序是对象的集合
	 */
}
