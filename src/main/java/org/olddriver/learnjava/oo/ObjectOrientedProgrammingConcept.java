package org.olddriver.learnjava.oo;

public class ObjectOrientedProgrammingConcept {
	/**
	 * 对象 Object
	 * 软件对象和现实中对象对应，对象可理解为是一种奇特变量，由属性和行为构成
	 * 
	 * 类 	Class
	 * 类是对具有相同属性和行为的对象的抽象，类是创建对象的蓝图；
	 * 类可以包含成员变量，成员方法，构造器，嵌套类，嵌套接口，构造块，静态块，常量？？？
	 * 类是java程序的基本单元
	 * 
	 * 接口	Interface
	 * 接口定义类与外界交互的规则，接口中可以包含常量，抽象方法，默认方法，static方法，嵌套类，嵌套接口？？？
	 * 
	 * 面向对象思想
	 * 将现实中对象抽象为类，在java程序中使用类的实例解决问题
	 * 
	 * 面向对象程序特点
	 * java程序是对象的集合
	 */
}
