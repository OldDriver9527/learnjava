public class ObjectOrientedProgrammingConcept{
	/**
	 * 面向对象，面向过程
     * 面向过程思考问题的方式比较直接，只关注编程的具体步骤
	 * 面向对象通过封装，继承，多态将编程的步骤分解到类或接口中，便于之后的复用和拓展
     * 面向对象相当于对面向过程的升级
     *
     * 类(Class)
     * 类是对具有相同属性和行为的一类事物的抽象描述，类是创建对象的蓝图；
     * 类成员包含字段(Field)，方法(Method)，类(Class)，接口(Interface)。
     * 类中还可以定义构造器(Constructor)，实例初始化器(Instance Initializer)，静态初始化器(Static Initializer)
     * 类中static成员变量称为类变量，非static成员变量称为实例变量
     * 类中static方法称为类方法，非static方法称为实例方法
     *
     * 对象(Object)
	 * 实际存在的物体为一个对象；程序中，对象是某个类的实例。对象由属性和行为构成，属性存储到字段中，行为定义到方法中。
	 * 对象可理解为是一种变量，既能存储数据，又能执行操作。
     *
	 * 接口(Interface)
	 * 接口定义类与外界交互的规则。
	 * 接口成员包含字段(public static final修饰)，方法，类，接口
	 */
}
