public class ObjectOrientedProgrammingConcept{
	/**
	 * 面向对象思想和面向过程思想
     * 面向过程思想以结构体和函数为编程基本单位。
     * 面向对象思想以类和对象为编程基本单位。类相当于结构体和函数的结合。将复杂问题分解成简单事物，再将事物抽象为类，
     * 在程序中通过对象相互协作实现功能。
     * 通过封装提高数据安全性，通过继承减少重复代码，通过多态实现易用
     *
     * 类(Class)
     * 类是对具有相同属性和行为的一类事物的抽象描述，类是创建对象的蓝图；
     * 类成员包含域(Field)，方法(Method)，类(Class)，接口(Interface)。
     * 类中还可以定义构造器(Constructor)，实例初始化器(Instance Initializer)，静态初始化器(Static Initializer)
     * 类中static域称为类变量，非static域称为实例变量
     * 类中static方法称为类方法，非static方法称为实例方法
     *
     * 对象(Object)
	 * 实际存在的物体为一个对象；程序中，对象是某个类的实例。对象由属性和行为构成，属性存储到域中，行为定义到方法中。
	 * 对象可理解为是一种奇特变量，既能存储数据，又能执行操作。
     *
	 * 接口(Interface)
	 * 接口定义类与外界交互的规则。
	 * 接口成员包含域(public static final修饰)，方法，类，接口
	 */
}
