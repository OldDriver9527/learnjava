public class Encapsulation {
    /*
     * 封装(Encapsulation)
     * 将字段，方法包裹在类中。
     * 封装也能理解为数据隐藏，设计类时通常将字段使用private修饰，意味着该字段对其他类隐藏，通过提供的public方法
     * 维护字段值
     * 封装好处
     * 类使用者无法触及类中隐藏部分
     * 类开发者对类中隐藏部分进行修改不会影响类的使用者
     *
     *
     *
     * classpath
     * 编译器和java虚拟机寻找字节码文件的根目录
     * 不建议设置classpath环境变量，而是将classpath作为参数传入
     *
     * jar
     * 实际是zip压缩文件
     * jar包中/META-INF/MANIFEST.MF文件可以定义启动类名称，不再需要运行jar文件时指定启动类名称
     */
}