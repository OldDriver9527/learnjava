public class Inheritance {
    /*
     * 继承(Inheritance)
	 * 在现有类的基础上派生出新类称为继承，是复用代码的一种方式
	 * 派生类称为子类，派生子类的类称为父类
	 *
	 * 类层次结构
	 * Object类是所有类的父类，定义类的通用行为；声明类时若不显式指明父类，默认继承Object类
	 * java中类支持单继承，不支持多继承，避免多个父类中成员冲突
	 *
	 * 对象转型
	 * 子类对象由子类类型转型为父类类型，称为向上转型，向上转型后，对象作为父类类型对待
	 * 子类对象由父类类型转型为子类类型，称为向下转型
	 * 向下转型前可以使用instanceof进行类型判断，避免RuntimeException
     */
}
