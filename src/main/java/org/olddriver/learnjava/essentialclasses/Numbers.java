public class Numbers {
    /*
     *
     * Number
     * 数字抽象类，是Byte，Short，Integer，Long，Float，Double父类
     *
     * 数字格式化输出
     * DecimalFormat 用于格式化十进制数字
     *
     * 自动装箱，自动拆箱(Autoboxing and Unboxing)
     * 自动装箱
     * 编译器将基本数据类型转换为对应包装类类型，使用包装类中valueOf方法进行自动装箱
     * 当为包装类类型变量赋值时可进行自动装箱
     * Byte，Short，Integer，Long，Character中valueOf方法
     * 当字面常量值在-128 ~ 127范围内，返回缓存数组中已创建的对象
     * 当在-128 ~ 127范围外，返回新创建对象
     * Double，Float中valueOf方法，返回新创建对象
     * Boolean中valueOf方法，返回已创建对象
     * 自动拆箱
     * 编译器将包装类类型转换为基本数据类型，使用包装类中xxxValue方法进行自动拆箱
     * 当为基本类型变量赋值或参与运算时可进行自动拆箱
     */
}
